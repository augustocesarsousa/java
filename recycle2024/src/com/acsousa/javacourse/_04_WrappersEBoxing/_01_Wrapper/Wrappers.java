package com.acsousa.javacourse._04_WrappersEBoxing._01_Wrapper;

public class Wrappers {

  /*
      Classes wrapper em Java são como caixas especiais que "embalam" tipos de dados primitivos.
      Elas fornecem funcionalidades extras que os dados primitivos por si só não possuem.
      Uma característica de uma classe wrapper é que o valor inicial delas é null.
     */

  // Wrapper para números inteiros
  Integer numeroWrapperInteiro;

  // Wrapper para números longos
  Long numeroWrapperLongo;

  // Wrapper para números com ponto flutuantes
  Double numeroWrapperDouble;

  // Wrapper para números com ponto flutuantes longos
  Float numeroWrapperFloat;

  // Wrapper para caractéres
  Character caracterWrapper;

  // Wrapper para boleanos
  Boolean booleanWrapper;

}
