package com.acsousa.javacourse._01_Fundamentos;

public class _02_OperadoresAritmeticos {
  public static void main(String[] args) {

    /*
      No Java os operadores aritmédicos básicos são cinco
      Adição (+)
      Subtração (-)
      Multiplicação (*)
      Divisão (/)
      Módulo ou resto (%), que é a sobra da divisão entre dois números
     */

    //Somando dois números
    System.out.println("15 + 2 = " + (15 + 13));

    //Subtraindo dois números
    System.out.println("15 - 2 = " + (15 - 13));

    //Multiplicando dois números
    System.out.println("15 * 2 = " + (15 * 2));

    //Dividindo dois números
    System.out.println("15 / 2 = " + (15 / 2));

    //Resto da divisão entre dois números
    System.out.println("15 % 2 = " + (15 % 2));
  }
}
