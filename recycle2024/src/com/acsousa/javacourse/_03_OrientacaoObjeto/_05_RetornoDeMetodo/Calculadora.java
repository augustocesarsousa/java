package com.acsousa.javacourse._03_OrientacaoObjeto._05_RetornoDeMetodo;

public class Calculadora {

  double numero1;
  double numero2;

  // Métodos que retornam o resultado das operações
  double soma() {
    return numero1 + numero2;
  }

  double subtracao() {
    return numero1 - numero2;
  }

  double multiplicacao() {
    return numero1 * numero2;
  }

  double divisao() {
    return numero1 / numero2;
  }
}
